package Module_1;

public class UnaryPlus {
    public static void main(String[] args) {
        
        int var = -90;

        int temp = var;

        System.out.println(temp);

        temp = -var; 

        System.out.println(temp);
    }
}

// Unary + is used for declaring +ve values (Which is not required as by default it takes +ve values)
// Unary - is used for declaring -ve values as well as converting positive values to -ve and vice versa
